#!/bin/bash

set -e

# colors
CRed='\033[1;31m'
CGray='\033[0;37m'
CGreen='\033[1;32m'
CYellow='\033[1;33m'
NC='\033[0m'

# logging
info() {
  echo -e "${CGray}${1}${NC}"
}

warn() {
  echo -e "${CYellow}${1}${NC}"
}

error() {
  echo -e "${CRed}${1}${NC}"
}

success() {
  echo -e "${CGreen}${1}${NC}"
}

# vars
os="${os:-${1}}"
arch="${arch:-${2}}"

info "downloading ..."
curl -fL "curl https://clickhouse.com/" -o "${TEMP_DIR}/install.sh"


info "installing ..."
sh "${TEMP_DIR}/install.sh"

info "cleanup ..."
rm "${TEMP_DIR}/install.sh"
